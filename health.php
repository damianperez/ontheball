<?php

//require_once "bootstrap.php";
require_once "config.php";
$start = microtime(true);

$checks = [];

/*
|--------------------------------------------------------------------------
| PHP
|--------------------------------------------------------------------------
*/

$checks["php"] = [
    "ok" => true,
    "version" => PHP_VERSION
];

/*
|--------------------------------------------------------------------------
| Config
|--------------------------------------------------------------------------
*/

$checks["config"] = [
    "ok" => defined("APP_VERSION"),
    "version" => defined("APP_VERSION") ? APP_VERSION : "N/D"
];

/*
|--------------------------------------------------------------------------
| Estado
|--------------------------------------------------------------------------
*/

$stateFile = DATA_DIR . "/estado.json";

$checks["state"] = [
    "ok" => file_exists($stateFile),
    "file" => basename($stateFile)
];

/*
|--------------------------------------------------------------------------
| Logs
|--------------------------------------------------------------------------
*/

$checks["logs"] = [
    "ok" => is_writable(LOG_DIR)
];

/*
|--------------------------------------------------------------------------
| Telegram
|--------------------------------------------------------------------------
*/

$checks["telegram"] = [
    "ok" => !empty(BOT_TOKEN)
];

/*
|--------------------------------------------------------------------------
| Tiempo
|--------------------------------------------------------------------------
*/

$response = round((microtime(true) - $start) * 1000, 2);

jsonResponse(
    true,
    [
        "message" => "Sistema saludable",
        "checks" => $checks,
        "response_ms" => $response,
        "server_time" => date("Y-m-d H:i:s")
    ],
    [
        "Health OK"
    ]
);